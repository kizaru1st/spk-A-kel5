        <h4>Penjelasan Singkat SAW</h4>
    Metode Simple Additive Weighting (SAW) merupakan salah satu algoritma dalam sistem pendukung keputusan. Algoritma SAW sering juga dikenal dengan istilah metode penjumlahan terbobot. Disebut dengan istilah tersebut, dikarenakan pada dasarnya SAW akan melakukan penjumlahan terbobot untuk semua atribut pada setiap alternatif. Tujuan akhirnya, supaya SAW bisa membandingkan alternatif secara lebih seimbang dan menghasilkan perhitungan yang lebih baik.
